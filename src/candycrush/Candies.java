
package candycrush;

public class Candies {
  private String rad = "\u2622";
  private String skull = "\u2620";   
  private String hazard="\u2623";
  private String yinyang="\u262F";
  private String paz="\u262E";

    public String getRad() {
        return rad;
    }

    public void setRad(String rad) {
        this.rad = rad;
    }

    public String getSkull() {
        return skull;
    }

    public void setSkull(String skull) {
        this.skull = skull;
    }

    public String getHazard() {
        return hazard;
    }

    public void setHazard(String hazard) {
        this.hazard = hazard;
    }

    public String getYinyang() {
        return yinyang;
    }

    public void setYinyang(String yinyang) {
        this.yinyang = yinyang;
    }

    public String getPaz() {
        return paz;
    }

    public void setPaz(String paz) {
        this.paz = paz;
    }  
  
}

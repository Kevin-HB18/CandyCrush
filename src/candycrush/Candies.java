
package candycrush;

public class Candies {
  private String rad = "\u2622";
  private String star = "\u272A";   
  private String sun="\u2742";
  private String yinyang="\u262F";
  private String paz="\u262E";

    public String getRad() {
        return rad;
    }

    public void setRad(String rad) {
        this.rad = rad;
    }

    public String getStar() {
        return star;
    }

    public void setStar(String star) {
        this.star = star;
    }   

    public String getSun() {
        return sun;
    }

    public void setSun(String sun) {
        this.sun = sun;
    }

  

    public String getYinyang() {
        return yinyang;
    }

    public void setYinyang(String yinyang) {
        this.yinyang = yinyang;
    }

    public String getPaz() {
        return paz;
    }

    public void setPaz(String paz) {
        this.paz = paz;
    }  
  
}
